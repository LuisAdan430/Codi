package net.cero.data;

public class FrecuenteConsultaPeticionOBJ {
	private String tipoServicio;
	private String ceActivo;
	private String cCuenta;
	private String ntActivo;

	public String getTipoServicio() {
		return tipoServicio;
	}
	public void setTipoServicio(String tipoServicio) {
		this.tipoServicio = tipoServicio;
	}
	public String getCeActivo() {
		return ceActivo;
	}
	public void setCeActivo(String ceActivo) {
		this.ceActivo = ceActivo;
	}
	public String getcCuenta() {
		return cCuenta;
	}
	public void setcCuenta(String cCuenta) {
		this.cCuenta = cCuenta;
	}
	public String getNtActivo() {
		return ntActivo;
	}
	public void setNtActivo(String ntActivo) {
		this.ntActivo = ntActivo;
	}
	
}
